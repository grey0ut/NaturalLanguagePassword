<#
    .SYNOPSIS
    A script for generating Natural Language style passphrases comprised of adjective/noun pairs. 
    .DESCRIPTION
    A Powershell script that can generate Diceware style passphrases. Can even add complexity using predefined character substitution. The built-in Get-Random cmdlet uses a predictable seed for random generation so instead the .NET RNGCryptoServiceProvider is utilized for as true of entropy as we can achieve. Each 'roll' is combined to form a 4 or 5 digit string (depending on noun vs adjective list) of numbers that is then used to select the corresponding word from the associated dictionary. 
    For simplicity the wordlists/dictionaries are stored within this script rather than as separate files. 
    .PARAMETER Pairs
    It generates Adjective/Noun pairs, this defines how many pairs you want returned. The default is 2 pair (two words)
    .PARAMETER TitleCase
    Switch parameter to toggle the use of title case. E.g. The First Letter Of Every Word Is Uppercase. 
    .PARAMETER Substitution
    Switch parameter to toggle common character substitution.  E.g. '3's for 'e's and '@'s for 'a's etc. 
    .PARAMETER Prepend
    Provide a string you would like prepended to the password output
    .PARAMETER Append
    Provide a string you would like appended to the password output
    .PARAMETER Delimiter
    This parameter accepts any string you would prefer as a delimiter between words. Defaults to a space.
    .PARAMETER IncludeNumber
    Switch parameter to randomly include a number within the passphrase
    .PARAMETER IncludeSymbol
    Switch parameter to randomly include a symbol within the passphrase
    .PARAMETER Shortcut
    A switch parameter to be used if calling this script from a shortcut link. The Target in the shortcut would look like this if the shortcut was in the same directory as this script:
    powershell.exe -noprofile -exec b -command "& {Clear; .\Get-NaturalLanguagePassword.ps1 -pairs 3 -Count 20 -Shortcut}"

    This will cause the script to leave the window open for the user to copy/paste the password. Then they can press 'Enter' and the window will close.
    .EXAMPLE
    C:\> .\New-NaturalLanguagePassword.ps1
    ripe scanner candied drunkard

    This will output a 4 word Natural Language password
    .EXAMPLE
    C:\> .\New-NaturalLanguagePassword.ps1 -Pairs 3
    deep bumps older gnats paltry tomb

    This will output 3 Adjective/Noun pairs (6 words)
    .EXAMPLE
    C:\> .\New-NaturalLanguagePassword.ps1 -Pairs 1 -Complexity Both
    L@rg3 Ph@nt0m

    This will output a single Adjective/Noun pair but with common character substitution.
    .EXAMPLE
    C:\> .\New-NaturalLanguagePassword.ps1 -Pairs 1 -Complexity UpperCase -Append "1@3"

    Large Phantom1@3

    This will output a single Adjective/Noun pair with capital first letters and the string "1@3" on the end
    .EXAMPLE
    C:\> .\New-NaturalLanguagePassword.ps1 -Pairs 2 -Complexity UpperCase -IncludeNumber -Count 5
    Inane8 Lords Stern Matron
    Metal Cafe Darkened6 Pang
    Erased Yearling Grim6 Pinky
    Crunchy Motivation Hateful 1Referee
    Wicked Binder Dire Antipasto6    

    This will create 5 passphrases with 2 Adjective/Noun pairs, capital first letters and a random number.
    .NOTES
    Version:        2.0
    Author:         C. Bodett
    Creation Date:  2/28/2025
    Purpose/Change: major rewrite.
